const adminLangCookie = "aigate_lang"

func resolveAdminLang(r *http.Request) adminLang {
	// Only honor user-set cookie; otherwise default to English.
	if c, err := r.Cookie(adminLangCookie); err == nil {
		switch c.Value {
		case "zh":
			return adminLangZH
		case "en":
			return adminLangEN
		}
	}
	return adminLangEN
}

func adminText(lang adminLang, key string) string {
	if lang == adminLangZH {
		if translated, ok := adminTranslations["zh"][key]; ok {
			return translated
		}
	}
	return key
}
